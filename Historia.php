        <h1 class="text-center text-primary">Historia</h1><br>
        <p class="text-center text-sm">
            Pocas personas saben cómo se gestó el nacimiento de la Copa de Europa que hizo grande, mítica, el Real Madrid.
            El origen fue, como casi todo lo que ha sucedido en Europa a lo largo de siglos, un exceso de orgullo entre
            países. Demasiadas veces, la soberbia de este Viejo Continente nos llevó a cruentas guerras. En esta ocasión,
            en los años 1953 y 1954, hastiados ya de dos conflictos mundiales, el orgullo suscitó la acción del torneo de
            fútbol más bonito del mundo.
            Todo nació cuando la prensa inglesa anunció, en 1954, que el Wolwerhampton era el mejor equipo del mundo. El
            conjunto inglés había ganado en casa al Spartak de Moscú y al mítico Honved de Budapest en dos partidos
            amistosos. Inmediatamente, Gabriel Hanot (en la foto), ex futbolista francés, ex seleccionador de su país y
            periodista de “L’Equipe”, contestó con pluma fina: “Para ser el mejor equipo del mundo hay que jugar también en
            campo del Honved y del Spartak, y enfrentarse igualmente a otros dos grandes del fútbol europeo, el Real Madrid y
            el Milán”. Y Hanot puso en marcha el gran sueño.
            En la primavera de 1955 se reunieron en un hotel de París el jefe de Gabriel Hanot en el periódico francés,
            Jacques de Ryswick, y Santiago Bernabéu (en la foto), presidente del Real Madrid. A su lado, Raimundo Saporta,
            ejercía de hacedor de lo imposible y de traductor. El asunto, tratar y dar forma a la idea que el prestigioso
            diario francés había lanzado al aire: la creación de un gran campeonato continental similar al Campeonato
            Sudamericano de Campeones, precursor de la Copa Libertadores. La Copa de Europa daba su primer paso. Pronto se
            unirían los demás
        </p><br>
        <h2 class="text-center text-primary">Copa Latina y Copa Mitrota</h2><br>
        <p class="text-center">
            Hasta ese momento, eran dos las competiciones de clubes en las que se ponía en juego la supremacía en Europa.
            Por un lado, la Copa Latina, creada en 1949, y que enfrentaba a los campeones de Portugal, España, Italia y
            Francia. Milán, Barcelona y Real Madrid fueron los dominadores, con dos títulos cada uno. Por el otro lado, la
            Copa Mitropa, reservada a clubes de la Europa central, y que había nacido mucho antes. Concretamente, en 1927,
            con lo que gozaba de mayor prestigio. Rapid de Viena, Ujpesti, Ferencvaros, Austria Viena y Sparta de Praga se
            repartieron casi todas las victorias.
        </p><br>
        <h2 class="text-center text-primary">La importancia de periódico francés “L’Equipe”</h2><br>
        <p class="text-center">
            En 1954 el Wolverhampton, campeón de la Premier, recibió y derrotó en su estadio al Honved de Budapest y al
            Spartak de Moscú, dos de los equipos más fuertes del momento, en sendos partidos amistosos, lo que llevó al
            “Daily Mail” a proclamarle "campeón del mundo de clubes".
            Gabriel Hanot, más tarde creador también del Balón de Oro, respondió desafiante al diario inglés: "Antes de
            decir que el Wolverhampton es invencible, tendrían que viajar a Moscú y a Budapest. Además, hay otros grandes
            equipos en Europa como el AC Milán o el Real Madrid. Puesto que se va observando el buen nivel de los equipos,
            habría que hacer un campeonato del mundo, o al menos uno a nivel europeo con más prestigio que la Copa Mitropa.
            Es algo necesario...".
            Al día siguiente, L'Equipe publica un editorial de Jacques de Ryswick, su jefe internacional, aceptando el
            desafío. Hanot, entonces, se reúne con sus compañeros y comienza a diseñar el proyecto de una competición donde
            los campeones de Liga de los respectivos países puedan disputarse la hegemonía del fútbol continental. En ese
            momento resultó vital la aparición de Santiago Bernabéu. Ante la falta de consolidación de la UEFA, creada unos
            meses antes, y la indiferencia de la FIFA, Hanot y los suyos contactaron con los dirigentes de los clubes de
            mayor importancia del continente. El presidente del Real Madrid, uno de los equipos más potentes del panorama
            mundial, fue quien más apoyó la nueva idea, pues vislumbró una competición con mucho margen de crecimiento, y
            que podría engrandecer a su propio club. También el Anderlecht mostró todo su apoyo, exigiendo, eso sí, que los
            partidos se jugaran entre semana para que no interfiriera en las ligas locales.
            Ante la importancia que estaba tomando el asunto, Ebbe Schwartz, entonces presidente de la UEFA, acabó cediendo,
            aunque impuso la obligación de que en el nombre de la competición no apareciera el término 'Europa', para evitar
            que restara protagonismo a la competición de selecciones. Nació así la Copa de Clubes Campeones Europeos.
            Para la primera edición se decidió crear una competición que se jugaría por un sistema de eliminatorias directas
            y a la cual acudirían 16 clubes invitados por una comisión creada para tal efecto. Dieciséis equipos disputaron
            la primera edición, en la temporada 1955-56. El Real Madrid consiguió la primera Copa de Europa. El éxito, por su
            repercusión, fue universal. Era la competición que definiría al mejor club de Europa.
            Para los siguientes años, sin embargo, se aceptó que únicamente acudieran a la competición los campeones de cada
            país, así como que los enfrentamientos se decidieran por sorteo. También se pretendía que la final se disputase
            cada año en el campo del equipo campeón, pero el encadenamiento de títulos por parte del Real Madrid hizo que se
            tuviera que variar esa idea, optándose por una sede que cambiara cada año.
            Después de contar con el apoyo de los grandes clubes, y sus dirigentes, y con la aprobación de la UEFA y la FIFA,
            sólo faltaba el apoyo mediático para rentabilizar la Copa de Campeones. De hecho, Jacques Ferrán, uno de los
            compañeros de Hanot en L’Equipe y de los que más le ayudó en su empresa, siempre aseguró que su objetivo era
            "que se jugara al fútbol más días, para poder vender más periódicos a mediados de la semana".
            Tal y como sucede hoy en día, la televisión tomó un papel primordial. Por aquel entonces se acababa de crear
            Eurovisión, la unión de televisiones europeas, y desde su comienzo dio todo el apoyo a la nueva competición,
            consciente de que se trataba de un producto de máximo interés a retransmitir. En poco tiempo, la cantidad de
            telespectadores deseosos de comprobar la supremacía futbolística de su equipo en el Viejo Continente se
            multiplicó hasta números exagerados.
            Quizá en ese momento Gabriel Hanot no era consciente de ello, pero acababa de aparecer, bajo su tutela, la
            competición de clubes más importante del mundo.
        </p><br>
